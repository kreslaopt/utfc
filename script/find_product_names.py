def update_mapping_from_filenames(root_dir):
    for root, dirs, files in os.walk(root_dir):
        for file in files:
            if file.endswith('.json'):
                product_name = os.path.splitext(file)[0]
                # Проверяем, есть ли уже такое значение в любом ключе словаря
                value_exists = any(
                    product_name.lower() == str(v).lower()
                    for v in NAME_MAPPING.values()
                    if v != "" and not isinstance(v, list)
                )
                if not value_exists:
                    # Проверяем, есть ли уже такое значение в списке пустого ключа
                    if product_name not in NAME_MAPPING['']:
                        NAME_MAPPING[''].append(product_name)
                        print(f'Добавлено: \'\'\': \'{product_name}\\''')
                # Если значение пустое - заполняем
                else:
                    for key in NAME_MAPPING:
                        if NAME_MAPPING[key] == '':
                            if product_name.lower() in key.lower() or key.lower() in product_name.lower():
                                NAME_MAPPING[key] = product_name
                                print(f'Заполнено: "{key}": "{product_name}"')

