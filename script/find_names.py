def normalize_name(name):
    """Приводит название к нормализованному виду для сравнения"""
    # Сначала проверяем, есть ли точное соответствие в словаре
    if name in NAME_MAPPING:
        name = NAME_MAPPING[name]
    # Если нет, то нормализуем как обычно
    name = name.lower().replace('/', '').replace('\\', '').replace('_', ' ').replace('.json', '').strip()
    words = sorted(name.split())
    return ' '.join(words)

def get_all_product_filenames(root_dir):
    """Собирает все названия файлов из папки и подпапок (без расширения)"""
    filenames = []
    for root, _, files in os.walk(root_dir):
        for file in files:
            if file.endswith('.json'):
                filename = os.path.splitext(file)[0]
                filenames.append(filename)
    return filenames

def main():
    # 1. Собираем названия файлов из папки
    products_dir = r'C:\Users\UTFC\Documents\БалтМебель\to\products'
    product_filenames = get_all_product_filenames(products_dir)
    normalized_product_names = {normalize_name(name) for name in product_filenames}

    # 2. Собираем IE_NAME из merged_result.json
    merged_file = r'C:\Users\UTFC\Documents\БалтМебель\to222\script\merged_result.json'
    try:
        with open(merged_file, 'r', encoding='utf-8') as f:
            merged_data = json.load(f)
    except Exception as e:
        logger.error(f"Ошибка загрузки merged_result.json: {e}")
        return

    # 3. Сравниваем
    not_found = []
    found = []
    for item in merged_data:
        parent_name = item.get('parent', {}).get('IE_NAME', '').strip()
        if not parent_name:
            logger.warning(f"Пустое IE_NAME в записи: {item}")
            continue
        normalized_parent = normalize_name(parent_name)
        if normalized_parent in normalized_product_names:
            found.append(parent_name)
        else:
            not_found.append(parent_name)

    # 4. Логируем результаты
    if found:
        logger.info("Найдены соответствия для следующих IE_NAME:")
        for name in found:
            logger.info(f"+ {name}")
    if not_found:
        logger.error("Не найдены соответствия для следующих IE_NAME:")
        for name in not_found:
            logger.error(f"- {name}")
    if not not_found:
        logger.info("Все IE_NAME имеют соответствия в папке products.")

if __name__ == '__main__':
    main()
